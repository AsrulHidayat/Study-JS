// 1. while Loop:
// Evaluasi kondisi di awal: Pada while loop, kondisi evaluasi dilakukan di awal sebelum blok pernyataan dijalankan. Jika kondisi awal adalah salah, blok pernyataan tidak akan dijalankan sama sekali.

// 2. do-while Loop:
// Evaluasi kondisi di akhir: Pada do-while loop, blok pernyataan akan dijalankan setidaknya satu kali bahkan jika kondisi evaluasi awalnya salah. Evaluasi kondisi dilakukan setelah blok pernyataan dijalankan.

// while
let i = 10 
while (i < 10){
    i++
    console.log('Asrul Hidayat')
}

// do-while
do {
    i++
    console.log('Ashiday suan')
} while (i < 10)